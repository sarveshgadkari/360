/* ---------------- Product filtering ---------------- */
const grid = document.getElementById("productGrid");
if (grid) {
  const selCompany = document.getElementById("fCompany");
  const selType = document.getElementById("fType");
  const selTech = document.getElementById("fTech");
  const search = document.getElementById("fSearch");
  const noRes = document.getElementById("noResults");
  const count = document.getElementById("resultCount");

  function applyFilters() {
    let shown = 0;
    grid.querySelectorAll(".product-card").forEach(card => {
      const okCompany = !selCompany.value || card.dataset.company === selCompany.value;
      const okType = !selType.value || card.dataset.type === selType.value;
      const okTech = !selTech.value || card.dataset.tech.includes(selTech.value);
      const okSearch = !search.value || card.textContent.toLowerCase().includes(search.value.toLowerCase());
      const show = okCompany && okType && okTech && okSearch;
      card.style.display = show ? "" : "none";
      if (show) shown++;
    });
    noRes.style.display = shown ? "none" : "block";
    count.textContent = shown + " product" + (shown === 1 ? "" : "s");
  }
  [selCompany, selType, selTech].forEach(s => s.addEventListener("change", applyFilters));
  search.addEventListener("input", applyFilters);
  applyFilters();
}

