/* ---------------- Auto lead-capture popup (Special Offer) ---------------- */
(function () {
  const pop = document.getElementById("offerPopup");
  if (!pop) return;
  // Fix pointer events on burst element
  const burst = pop.querySelector(".burst");
  if (burst) burst.style.pointerEvents = "none";
  let seen = false;
  let footerPromptDone = false;
  try { seen = sessionStorage.getItem("h360_offer_seen") === "1"; } catch (e) {}
  try { footerPromptDone = sessionStorage.getItem("h360_offer_footer_seen") === "1"; } catch (e) {}
  function showPop(force) {
    if (document.querySelector(".modal.open")) return;
    if (!force && seen) return;
    pop.classList.add("open");
    document.body.style.overflow = "hidden";
    seen = true;
    try { sessionStorage.setItem("h360_offer_seen", "1"); } catch (e) {}
  }
  function hidePop() {
    pop.classList.remove("open");
    document.body.style.overflow = "";
  }
  setTimeout(() => showPop(false), 5000); // auto-open after 5s on first visit
  // exit-intent (desktop)
  document.addEventListener("mouseout", e => {
    if (!e.relatedTarget && e.clientY <= 0) showPop(false);
  });
  // Last-chance prompt: user has scrolled all the way to the footer (works on mobile too,
  // where exit-intent can't fire) — show again even if already seen/dismissed once.
  const footerEl = document.querySelector(".footer");
  if (footerEl) {
    const footerIO = new IntersectionObserver(entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting && !footerPromptDone) {
          footerPromptDone = true;
          try { sessionStorage.setItem("h360_offer_footer_seen", "1"); } catch (e) {}
          showPop(true);
          footerIO.unobserve(footerEl);
        }
      });
    }, { threshold: .15 });
    footerIO.observe(footerEl);
  }
  pop.addEventListener("click", e => { if (e.target === pop) hidePop(); });
  pop.querySelector(".modal-close").addEventListener("click", hidePop);
  document.querySelectorAll("[data-open-offer]").forEach(b => b.addEventListener("click", () => showPop(true)));
  document.getElementById("offerForm").addEventListener("submit", e => {
    e.preventDefault();
    const f = e.target;
    const msg =
      "SPECIAL OFFER claim — Hearing 360 Degree website%0A" +
      "Offer: FREE Hearing Test + FREE Hearing Aid Trial%0A" +
      "Name: " + encodeURIComponent(f.name.value) + "%0A" +
      "Phone: " + encodeURIComponent(f.phone.value);
    window.open("https://wa.me/" + WA_NUMBER + "?text=" + msg, "_blank");
    hidePop();
    f.reset();
  });
})();

